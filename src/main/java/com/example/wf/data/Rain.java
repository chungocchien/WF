package com.example.wf.data;

public class Rain {
    private double threeH;

    public void setThreeH(double threeH) {
        this.threeH = threeH;
    }

    public double getThreeH() {
        return threeH;
    }

    @Override
    public String toString() {
        return "Rain{" +
                "threeH=" + threeH +
                '}';
    }
}
